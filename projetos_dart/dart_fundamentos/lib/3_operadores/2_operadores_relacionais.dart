void main() {
  // Operadores relacionais
  // Temos 6 tipos
  // == (Igualdade), != (diferença), > (Maior que), < (Menor que) , <= (Menor igual), >= (Maior igual)
  final idade = 18;

  if (idade == 18) {
    print('Pode tirar habilitação.');
  }

  if (idade >= 18) {
    print('Pode tirar habilitação.');
  }

  if (idade > 17) {
    print('Pode tirar habilitação.');
  }

  if (idade < 18) {
    print('Não pode tirar habilitação.');
  }
}
